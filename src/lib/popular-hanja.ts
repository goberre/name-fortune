export function isPopularHanja(hangul: string, hanja: string): boolean {
  const list = POPULAR_BY_SYLLABLE[hangul];
  if (list) {
    const idx = list.indexOf(hanja);
    if (idx !== -1 && idx < 6) return true;
  }
  return GLOBAL_POPULAR.has(hanja);
}

export function sortCandidates<T extends { hanja: string; meaning: string }>(
  hangul: string,
  items: T[],
): T[] {
  const priority = POPULAR_BY_SYLLABLE[hangul] ?? [];
  return [...items].sort((a, b) => {
    const ai = priority.indexOf(a.hanja);
    const bi = priority.indexOf(b.hanja);
    if (ai !== -1 && bi !== -1) return ai - bi;
    if (ai !== -1) return -1;
    if (bi !== -1) return 1;
    if (GLOBAL_POPULAR.has(a.hanja) && !GLOBAL_POPULAR.has(b.hanja)) return -1;
    if (!GLOBAL_POPULAR.has(a.hanja) && GLOBAL_POPULAR.has(b.hanja)) return 1;
    return a.meaning.length - b.meaning.length;
  });
}
